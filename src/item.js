class Item {
  constructor(
    id,
    name,
    titleName,
    price,
    department,
    color,
    material,
    description
  ) {
    this.id = id;
    this.titleName = titleName;
    this.name = name;
    this.price = price;
    this.department = department;
    this.color = color;
    this.material = material;
    this.description = description;
  }
}

export default Item;
